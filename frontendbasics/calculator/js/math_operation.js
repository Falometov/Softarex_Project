"use strict";

class MathOperation {

    static sum(number1, number2) {
        return number1 + number2;
    }

    static difference(number1, number2) {
        return number1 - number2;            
    }

    static division(number1, number2) {
        return number1 / number2;
    }

    static multiple(number1, number2) {
        return number1 * number2;
    }
    
}